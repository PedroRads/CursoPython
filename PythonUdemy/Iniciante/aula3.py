print(1234)

#Aspas simples, se tiver aspas duplas dentro de aspas simples aparecera na saida as aspas duplas
print('Pedro lucas')
print(1, 'Pedro "Lucas"')


#Aspas Duplas, se tiver aspas simples dentro de aspas duplas aparecera na saida as aspas simples
print("Pedro Lucas")
print(1, "Pedro 'Lucas'")

#Escape, o escape permite mostrar simbolos que normalmente não iria aparecer na saida
print("Pedro \"Lucas\"")

#r, o 'r' faz com que simbolos e acentos apareceram na saida que normalmente não iria aparecer
print(r"Pedro \"Lucas\"")
